package com.chuxuezhe.shape;

public class Point {
	
	private int m;
	private int n;

	public Point(int m,int n) {
		this.m=m;
		this.n=n;
	}

	public int getM() {
		return m;
	}

	public void setM(int m) {
		this.m = m;
	}

	public int getN() {
		return n;
	}

	public void setN(int n) {
		this.n = n;
	}
	
	

}
